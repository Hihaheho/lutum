/// Top-N most likely token at a given position with its log probability.
///
/// ```
/// use lutum_openai::chat::TopLogprob;
/// use serde_json::Value;
///
/// let json = serde_json::from_str::<Value>(
///     r#"{"token":"hello","bytes":[104,101,108,108,111],"logprob":-0.5}"#,
/// )
/// .unwrap();
/// let val = serde_json::from_value::<TopLogprob>(json.clone()).unwrap();
/// assert_eq!(serde_json::to_value(&val).unwrap(), json);
/// assert_eq!(serde_json::from_value::<TopLogprob>(json).unwrap(), val);
/// ```
#[derive(Clone, Debug, PartialEq, Serialize, Deserialize)]
pub struct TopLogprob {
    pub token: String,
    pub bytes: Vec<u8>,
    pub logprob: f64,
}

