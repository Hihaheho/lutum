/// Processing tier used to serve a request.
///
/// ```
/// use lutum_openai::chat::ServiceTier;
/// use serde_json::Value;
///
/// let json = serde_json::from_str::<Value>(r#""auto""#).unwrap();
/// let val = serde_json::from_value::<ServiceTier>(json.clone()).unwrap();
/// assert_eq!(serde_json::to_value(&val).unwrap(), json);
/// assert_eq!(serde_json::from_value::<ServiceTier>(json).unwrap(), val);
/// ```
#[derive(Clone, Copy, Debug, Eq, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum ServiceTier {
    Auto,
    Default,
    Flex,
    Scale,
    Priority,
}

/// URL citation annotation returned when using the web search tool.
