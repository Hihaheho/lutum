/// A complete chat completion response from the model.
///
/// ```
/// use lutum_openai::chat::ChatCompletion;
/// use serde_json::Value;
///
/// let json = serde_json::from_str::<Value>(
///     r#"{"id":"chatcmpl-abc","choices":[{"finish_reason":"stop","index":0,"logprobs":null,"message":{"content":"Hello!","refusal":null,"role":"assistant"}}],"created":1741569952,"model":"gpt-4o","object":"chat.completion"}"#,
/// )
/// .unwrap();
/// let val = serde_json::from_value::<ChatCompletion>(json.clone()).unwrap();
/// assert_eq!(serde_json::to_value(&val).unwrap(), json);
/// assert_eq!(serde_json::from_value::<ChatCompletion>(json).unwrap(), val);
/// ```
#[derive(Clone, Debug, PartialEq, Serialize, Deserialize)]
pub struct ChatCompletion {
    pub id: String,
    pub choices: Vec<ChatCompletionChoice>,
    pub created: u64,
    pub model: String,
    pub object: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub service_tier: Option<ServiceTier>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub system_fingerprint: Option<String>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub usage: Option<CompletionUsage>,
}
